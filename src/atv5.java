public class atv5 {
    public static void main(String[] args) {
        String primeiroNome = "Messi";
        String sobrenome = "Silva";
        int idade = 60;
        char inical = primeiroNome.charAt(0);

        System.out.println("Ficha Cadastral: ");
        System.out.println("Nome completo: " + primeiroNome + " " + sobrenome);
        System.out.println("Idade: " + idade);
        System.out.println("Inicial do nome: " + inical);
        System.out.println("Olá, meu nome é " + primeiroNome + " " + sobrenome + ", tenho " + idade + " anos e meu nome começa" +
                "com " + inical + ".");
    }

}
